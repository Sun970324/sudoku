/// One mined board per technique for the debug-only "힌트 데모" entry: on
/// each board, calling that technique's finder directly (on fresh
/// auto-filled notes) fires with a conclusion matching the real solution.
/// Mined offline via the real generator pipeline at several dig depths;
/// regenerate an entry the same way if an engine change breaks it —
/// test/debug/technique_demos_test.dart guards every one.
///
/// The lone exception is BUG+1: its whole-grid "every other empty cell is
/// bivalue" precondition never arises on fresh candidates of a raw board
/// (0 hits mining 3000 boards × 5 dig depths), so its demo reuses the
/// mid-solve board + tracked-notes fixture from hint_engine_test —
/// [techniqueDemoNotes] supplies the notes the demo must load instead of
/// auto-filling.
///
/// Debug builds only — nothing in a release build references this.
SudokuPuzzle? techniqueDemoPuzzle(HintTechnique technique) {
  final givens = _demoGivens[technique];
  if (givens == null) return null;
  final board = givens.map((row) => List<int>.from(row)).toList();
  final solution = SudokuSolver().solve(board)!;
  return SudokuPuzzle(
    puzzle: SudokuGrid(board),
    solution: SudokuGrid(solution),
    fixedMask: List.generate(
        9, (r) => List.generate(9, (c) => board[r][c] != 0)),
    difficulty: techniqueDifficulty[technique]!,
  );
}

bool techniqueDemoAvailable(HintTechnique technique) =>
    _demoGivens.containsKey(technique);

/// Pre-narrowed notes a demo must load instead of auto-filling, or null
/// for the (normal) fresh-notes techniques. Only BUG+1 needs this.
List<List<Set<int>>>? techniqueDemoNotes(HintTechnique technique) {
  if (technique != HintTechnique.bugPlusOne) return null;
  final notes = List.generate(9, (_) => List.generate(9, (_) => <int>{}));
  _bugPlusOneNotes.forEach((cell, digits) {
    notes[cell.$1][cell.$2] = {...digits};
  });
  return notes;
}

